/*performing switch case having predefine parameter*/
public class Switch
{
public static void main(String args[])
	{

char ch;
ch='A';
System.out.println("Select student grade is");

switch(ch)				//switch
	{

case 'A' :
	
	System.out.println("Excellent");
		break; 
	
case 'B' : 
	System.out.println("Good");
		break;

case 'C' :
	System.out.println("Average");
		break;

}
}
}


